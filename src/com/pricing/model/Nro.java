package com.pricing.model;

public class Nro {
	public static int nroImagenes;
	public static void incrementarNroImagenes()
	{
		nroImagenes++;
	}
	public static void decrementarNroImagenes()
	{
		nroImagenes--;
	}
	public static void inicializarNroImagenes()
	{
		nroImagenes=0;
	}
}
